

from pathlib import Path
import numpy as np
import pandas as pd


folder = Path("IBEX35")
prices = {}

for file in folder.glob("*.csv"):
    df = pd.read_csv(file, index_col=0)
    priceCol = None
    
    for col in ["Close", "close", "Adj Close", "adj close", "Price", "price"]:
        if col in df.columns:
            priceCol = col
            break

    if priceCol is None:
        numCols = df.select_dtypes(include=[np.number]).columns
        if len(numCols) == 0:
            continue
        priceCol = numCols[-1]

    prices[file.stem] = pd.to_numeric(df[priceCol], errors="coerce")

pricesDF = pd.DataFrame(prices).ffill().dropna()
DFRets = pricesDF.pct_change().dropna()

print("Price summary")
print(pricesDF.describe().T[["mean", "std", "min", "max"]])

print("\nReturn summary")
print(DFRets.describe().T[["mean", "std", "min", "max"]])

print("\nVariance of returns")
print(DFRets.var())

print("\nCorrelation matrix of returns")
print(DFRets.corr())

print("\nCovariance matrix of returns")
print(DFRets.cov())


